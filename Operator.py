A=float(input("please enter the value of A:"))
B=float(input("please enter the value of B:"))
opérator=input("please enter the operator:")
if opérator =="+":
    print("A+B",A+B)
elif opérator =="-":
    print("A-B",A-B)
elif opérator =="*":
    print("A*B",A*B)
elif opérator =="/":
    print("A/B",A/B)
else :
    print("division by 0 is impossible")
    print("the operator is incorrect")
